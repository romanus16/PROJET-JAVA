    private void initialiserMonInterface() {
        // Gestion des alertes stock 
        
        itemStockAlerte.addMouseListener(new java.awt.event.MouseAdapter() {
        @Override
        public void mouseClicked(java.awt.event.MouseEvent e) {
            try {
                ProduitDAO dao = new ProduitDAO();
                var produits = dao.produitsEnAlerte();
                if (produits.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Aucun produit en alerte de stock.");
                } else {
                    StringBuilder sb = new StringBuilder("ðŸ“¢ PRODUITS EN ALERTE DE STOCK :\n\n");
                    for (var produit : produits) {
                        sb.append("â€¢ ").append(produit.getNom()).append(" (").append(produit.getStockActuel()).append(")\n");
                    }
                    JOptionPane.showMessageDialog(null, sb.toString(), "Alerte Stock", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erreur: " + ex.getMessage());
            }
        }
   
        
         });
    }
    
    private void chargerDonnees() {
        try {
            ProduitDAO produitDAO = new ProduitDAO();
            var produitsAlerte = produitDAO.produitsEnAlerte();
            
            DefaultTableModel modelAlertes = new DefaultTableModel(
                new Object[]{"Produit", "CatÃ©gorie", "Stock", "Seuil"}, 0
            );
            
            for (var produit : produitsAlerte) {
                modelAlertes.addRow(new Object[]{
                    produit.getNom(),
                    produit.getCategorie().getLibelle(),
                    produit.getStockActuel(),
                    produit.getSeuilAlerte()
                });
            }
            tableProduitsAlerte.setModel(modelAlertes); // [cite: 51]
            
        } catch (Exception e) {
            System.err.println("Erreur chargement: " + e.getMessage());
        }
    }

